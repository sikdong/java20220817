package ch03.lecture.p2binary;

public class C09Assignment {
	public static void main(String[] args) {
		
		// 대입 연산자 : =, +=,-=,*=,%=
		
		int i = 3;
		int j = 5;
		
		int k = i;
		
		k +=j; // k = k + j;
		
		System.out.println(k);
	}
	
	// 대입 연산의 진행방향은 오른쪽
	
//	a = b = c = 5 (C = 5부터 시작해서 오른쪽으로)
	
}
